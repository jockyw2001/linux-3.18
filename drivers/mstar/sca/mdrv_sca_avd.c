static MS_BOOL MApp_AVD_Init(AVDINIT_AUX_CBF pfPostTestCbf, MS_U16 u16Retry, MS_BOOL bForceInit)
{
	VD_INITDATA sVD_InitData;

	static MS_BOOL _bAvdInitReady  = FALSE;

	const MS_U8 _u8VdDecInitializeExt[] =
	{
		//initial AFEC Setting
		_RV1(0x35D4, 0x00),  // TODO update correct value
		_RV1(0x35D5, 0x60),
		//_RV1(BK_AFEC_D8, 0x88),
		//_RV1(BK_AFEC_D9, 0x19),
		_END_OF_AVD_TBL_,
	};


	if (_bAvdInitReady && !bForceInit)
	{
		return TRUE;
	}

	// Init VD
	// VIF
	sVD_InitData.u32XTAL_Clock = MST_XTAL_CLOCK_HZ; // TODO replace with system define
	sVD_InitData.eDemodType = DEMODE_MSTAR_VIF;

	// afec
	sVD_InitData.eLoadCodeType=AVD_LOAD_CODE_BYTE_WRITE;
    sVD_InitData.u32VD_DSP_Code_Address=0;
	sVD_InitData.pu8VD_DSP_Code_Address=(MS_U8 *)&DSP_CODE;
	sVD_InitData.u32VD_DSP_Code_Len=sizeof(DSP_CODE);
	sVD_InitData.u16VDDSPBinID = 0;

	sVD_InitData.bRFGainSel = VD_USE_AUTO_GAIN;
	sVD_InitData.bAVGainSel = VD_USE_AUTO_GAIN;
	sVD_InitData.u8RFGain = 0x58;
	sVD_InitData.u8AVGain = 0x58;

	sVD_InitData.u32VDPatchFlag = AVD_PATCH_HTOTAL_1135|AVD_PATCH_FINE_TUNE_FH_DOT|AVD_PATCH_FINE_TUNE_COMB_F2|AVD_PATCH_FINE_TUNE_3D_COMB|AVD_PATCH_NON_STANDARD_VTOTAL|AVD_PATCH_FINE_TUNE_STILL_IMAGE;
	sVD_InitData.u8ColorKillHighBound = 0x18;
	sVD_InitData.u8ColorKillLowBound = 0x0E;

	sVD_InitData.u8SwingThreshold = 2;
	sVD_InitData.eVDHsyncSensitivityNormal.u8DetectWinBeforeLock= 0x08;
	sVD_InitData.eVDHsyncSensitivityNormal.u8DetectWinAfterLock= 0x08;
	sVD_InitData.eVDHsyncSensitivityNormal.u8CNTRFailBeforeLock= 0x0F;
	sVD_InitData.eVDHsyncSensitivityNormal.u8CNTRSyncBeforeLock= 0x10;
	sVD_InitData.eVDHsyncSensitivityNormal.u8CNTRSyncAfterLock= 0x1C;
	sVD_InitData.eVDHsyncSensitivityTuning.u8DetectWinBeforeLock= 0x04;
	sVD_InitData.eVDHsyncSensitivityTuning.u8DetectWinAfterLock= 0x04;
	sVD_InitData.eVDHsyncSensitivityTuning.u8CNTRFailBeforeLock= 0x08;
	sVD_InitData.eVDHsyncSensitivityTuning.u8CNTRSyncBeforeLock= 0x10;
	sVD_InitData.eVDHsyncSensitivityTuning.u8CNTRSyncAfterLock= 0x1C;
	sVD_InitData.u8VdDecInitializeExt=(MS_U8 *)&_u8VdDecInitializeExt;

	// comb

    AVD_KDBG("Comb 3D Buff addr %lx, size: %lx \r\n", g3DComBufAddr, g3DComBufSize);
	sVD_InitData.u32COMB_3D_ADR = g3DComBufAddr;
	sVD_InitData.u32COMB_3D_LEN = g3DComBufSize;

    if(sVD_InitData.u32VDPatchFlag & AVD_PATCH_FINE_TUNE_STILL_IMAGE)
    {
        AVD_Still_Image_Param param;
        param.bMessageOn =0;

        param.u8Threshold1 = 0x14;
        param.u8Threshold2 = 0x19;
        param.u8Threshold3 = 0x29;
        param.u8Threshold4 = 0x30;

        param.u8Str1_COMB37 = 0x88;
        param.u8Str1_COMB38 = 0xF8;
        param.u8Str1_COMB7C = 0x10;
        param.u8Str1_COMBED = 0x80;

        param.u8Str2_COMB37 = 0x8F;
        param.u8Str2_COMB38 = 0xF8;
        param.u8Str2_COMB7C = 0x20;
        param.u8Str2_COMBED = 0x82;

        param.u8Str3_COMB37 = 0x0B;
        param.u8Str3_COMB38 = 0xF8;
        param.u8Str3_COMB7C = 0x10;
        param.u8Str3_COMBED = 0x80;

        MDrv_AVD_SetStillImageParam(param);
    }


	if (E_AVD_OK != MDrv_AVD_Init(&sVD_InitData, sizeof(sVD_InitData)))
	{
		AVD_VER_FAIL_MSG();
		_bAvdInitReady = FALSE;
		goto mapp_avd_init_end;
	}

	//load DSP code
	MDrv_AVD_SetInput(E_INPUT_SOURCE_CVBS1, SCART_FB_NONE);

	if (!pfPostTestCbf)
	{
		_bAvdInitReady = TRUE;
	}
	else
	{
		_bAvdInitReady = pfPostTestCbf(u16Retry);
	}

    _gbAVDInit = TRUE;

mapp_avd_init_end:

	return _bAvdInitReady;

}


MS_U16 GetMailBoxStatus()
{
	MS_U16 u16ShrMbxStatus = 0xFFFF;
	MS_U32 u32ModuleBaseAddr =0, u32ModuleBaseSize =0;

	MDrv_MMIO_GetBASE( &u32ModuleBaseAddr, &u32ModuleBaseSize, MS_MODULE_PM);
	u16ShrMbxStatus = INREG16( u32ModuleBaseAddr + (REG_SHR_MBX_ADDR<<1) );

	AVD_KDBG("Get Mbx: 0x%X \r\n", u16ShrMbxStatus);

	return u16ShrMbxStatus;
}

static MS_BOOL IsDSPRunning(MS_U16 a_u16RetryLimit)
{
	MS_U16 u16Pass = 0;
	MS_U8  u8Value;
	MS_BOOL   bRet    = FALSE;

	u8Value=MDrv_AVD_GetRegValue(0x35D6);
	for (u16Pass=0; u16Pass<a_u16RetryLimit; u16Pass++)
	{
		if (u8Value == MDrv_AVD_GetRegValue(0x35D6))
		{
			mdelay(10);
		}
		else
		{
			bRet = TRUE;	// !!! DSP is running !!!
			break;
		}
	}

	return bRet;
}

