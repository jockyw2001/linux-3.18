/*----------------------------------------------------------------------------------------------------------
 *
 * Function: Hal_SD_TIMER_mSleep
 *     @author jeremy.wang (2015/1/15)
 * Desc: Run milli-second sleep
 *
 * @param u32_msec : Millisecond for Sleep
 *
 * @return U32_T  : Delay time
 ----------------------------------------------------------------------------------------------------------*/
U32_T Hal_SD_TIMER_mSleep(U32_T u32_msec)
{

    msleep(u32_msec);
    return u32_msec;

}

