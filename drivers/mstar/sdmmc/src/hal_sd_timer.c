/*----------------------------------------------------------------------------------------------------------
 *
 * Function: Hal_SD_TIMER_mDelay
 *     @author jeremy.wang (2017/12/4)
 * Desc: Run milli-second delay
 *
 * @param u32_msec : Milli-second for Delay
 *
 * @return U32_T  : Delay time
 ----------------------------------------------------------------------------------------------------------*/
U32_T Hal_SD_TIMER_mDelay(U32_T u32_msec)
{

//###########################################################################################################
#if (D_OS == D_OS__LINUX) || (D_OS == D_OS__UBOOT)
//###########################################################################################################
    mdelay(u32_msec);
    return u32_msec;
//###########################################################################################################
#elif (D_OS == D_OS__ROM)
//###########################################################################################################
    loop_delay_timer(TIMER_DELAY_1ms * u32_msec);
//###########################################################################################################
#elif (D_OS ==D_OS__YBOX)
//###########################################################################################################
    MsOS_DelayTask(u32_msec);
//###########################################################################################################
#elif (D_OS == D_OS__RTK)
//###########################################################################################################
    SYS_UDELAY(u32_msec * 1000);
//###########################################################################################################
#endif

    return 0;

}


/*----------------------------------------------------------------------------------------------------------
 *
 * Function: Hal_SD_TIMER_uDelay
 *     @author jeremy.wang (2017/12/4)
 * Desc: Run micro-second delay
 *
 * @param u32_usec : Micro-second for Delay
 *
 * @return U32_T  : Delay time
 ----------------------------------------------------------------------------------------------------------*/
U32_T Hal_SD_TIMER_uDelay(U32_T u32_usec)
{

//###########################################################################################################
#if (D_OS == D_OS__LINUX) || (D_OS == D_OS__UBOOT)
//###########################################################################################################
    udelay(u32_usec);
    return u32_usec;
//###########################################################################################################
#elif (D_OS == D_OS__ROM)
//###########################################################################################################
    loop_delay_timer(TIMER_DELAY_1us * u32_usec);
//###########################################################################################################
#elif (D_OS == D_OS__RTK)
//###########################################################################################################
    SYS_UDELAY(u32_usec);
//###########################################################################################################
#endif

    return 0;

}


